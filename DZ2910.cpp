#include <iostream> 
#include <math.h> 
using namespace std; 
 
int main() 
{ 
     double x, y; 
     for(int i = 0; i < 5; i++) 
     { 
          switch(i) { 
            case 0: 
                x = -2.5; 
                y = -0.1; 
                break; 
            case 1: 
                x = -1.5; 
                y = -0.1; 
                break;   
            case 2: 
                x = -0.5; 
                y =  0.5; 
                break; 
            case 3: 
                x =  1.5; 
                y =  0.5; 
                break; 
            case 4: 
                x =  0.5; 
                y =  1.1; 
                break; 
             
            } 
      
  
   
     if ((pow(x,2) + pow(y,2) >= 1) && (y <= 0.5*x+1) && (y >= -0.5*x-1)) 
  { 
      cout << "\nТочка лежит в заданной фигуре\n"; 
  } 
    else 
  { 
      cout << "\nТочка не лежит в заданной фигуре\n"; 
  } 
     } 
}
